print('Convert to binary:')
print('4 to binary: %s' %bin(4)) # 4 
print('0o10 to binary: %s' %bin(0o10)) # 8
print('0xA to binary: %s\n' %bin(0xA)) # 10 

print('Convert to decimalism:')
print('0b100 to decimalism: %s' %int(0b100)) # 4 
print('0o10 to decimalism: %s' %int(0o10)) # 8
print('0xA to decimalism: %s\n' %int(0xA)) # 10 

print('Convert to hexadecimal:')
print('0b100 to hexadecimal: %s' %hex(0b100)) # 4 
print('0o10 to hexadecimal: %s' %hex(0o10)) # 8
print('0xA to hexadecimal: %s\n' %hex(0xA)) # 10 

print('Convert to octonary:')
print('0b100 to octonary: %s' %oct(0b100)) # 4 
print('0o10 to octonary: %s' %oct(0o10)) # 8
print('0xA to octonary: %s' %oct(0xA)) # 10 
